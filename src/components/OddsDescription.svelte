<center>
  <h1>Odds Information</h1>
</center>
<p>
  The odds page shows, for a given team and turn, statistics about the team
  overlayed on the map. The different map types are as follows:
</p>
<ul>
  <li value="chance">
    <b>Chance:</b> The likelihood of a team winning a territory (i.e. what portion
    of the overall starpower that team had for each territory).
  </li>
  <li value="players">
    <b>Players:</b> The number of players that a team submitted to each territory.
  </li>
  <li value="wins">
    <b>Wins:</b> The territories that the team won are shown in green, while the
    territories the team lost are shown in red.
  </li>
  <li value="ones">
    <b>✯:</b> The number of players that a team submitted to each territory who had
    one star at the time of the roll.
  </li>
  <li value="twos">
    <b>✯✯:</b> The number of players that a team submitted to each territory who
    had two stars at the time of the roll.
  </li>
  <li value="threes">
    <b>✯✯✯:</b> The number of players that a team submitted to each territory who
    had three stars at the time of the roll.
  </li>
  <li value="fours">
    <b>✯✯✯✯:</b> The number of players that a team submitted to each territory who
    had four stars at the time of the roll.
  </li>
  <li value="fives">
    <b>✯✯✯✯✯:</b> The number of players that a team submitted to each territory who
    had five stars at the time of the roll.
  </li>
  <li value="teamPower">
    <b>Team Power:</b> Where the team deployed the most power that turn.
  </li>
  <li value="territoryPower">
    <b>Territory Power:</b> Where all teams deployed the most power that turn.
  </li>
</ul>
<p>
  For this map, red is higher (greater chance/number of players/etc.) while
  green is lower.
</p>
